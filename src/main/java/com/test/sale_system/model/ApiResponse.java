package com.test.sale_system.model;

public class ApiResponse<T> {
    private boolean success; // Indicates if the operation was successful
    private String message;  // A message describing the result
    private T data;          // The data returned by the operation (optional)

    public ApiResponse(boolean success, String message, T data) {
        this.success = success;
        this.message = message;
        this.data = data;
    }

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }
}
